//
// UTF-8 编码用于将执行字符集的字符映射到带 u8 前缀的字符常量（C23 起）或字符串字面量。实
// 现定义的编码（C23 之前）/ UTF-16 编码（C23 起）用于将执行字符集的字符映射到带 u 前缀的
// 字符常量或字符串字面量。实现定义的编码（C23 之前）/ UTF-32 编码（C23 起）用于将执行字符
