// 自顶向下分析法（top-down parsing）
//
// 自顶向下分析法可以看作是为输入串构造语法分析树的问题，它从语法分析树根节点开始，按照
// 先根顺序（深度优先地）创建这颗语法分析树的各个节点。自顶向下分析法也可以看成是寻找输
// 入串的最左推导的过程。自顶向下分析的每一步，其关键问题是确定对一个语法变量 A 应用哪
// 个产生式。一旦选择了某个 A 的产生式，语法分析过程的其余部分负责将相应产生式中的终结
// 符和输入相匹配。确定使用语法变量的哪一个产生式，可以使用回溯法，或者预测分析法。使用
// 预测的自顶向下分析法，根据一个文法的 FIRST 和 FOLLOW 集合，可以构造出预测分析表，它
// 说明了如何在自顶向下语法分析过程中选择产生式，这些集合也可以用于自底向上分析。
//
// 递归下降分析法（recursive decent parsing）是一种使用函数递归的自顶向下分析法，这种
// 方法可能需要进行回溯，以找到要应用的正确 A 产生式。然而，可以使用预测分析法来避免回
// 溯，预测分析法通过在输入中向前看固定多个符号来选择正确的 A 产生式，通常情况下我们只
// 需要向前看一个符号（即只看下一个输入终结符），即 LL(1) 文法，第一个 L 表示从左向右
// 处理输入，第二个 L 表示执行最左推导，如果带有数字表示向前看几个符号来预测需要选用哪
// 个产生式进行推导。对于 LL(1) 文法，我们可以构造出预测分析器，即不需要回溯的递归下降
// 语法分析器。对于有些文法，我们也可以构造出向前看 k 个输入符号的预测分析器，这一类文
// 法也称为 LL(k) 文法。当然，我们也可以使用非递归的语法分析法，它显式地维护一个栈，而
// 不是通过递归调用隐式地维护一个栈。
//
// 一个递归下降语法分析程序由一组过程组成，每个语法变量就对应一个过程，程序的执行从起始
// 符号对应的过程开始，如果这个过程的函数体扫描了整个输入串，它就停止执行并宣告语法分析
// 成功完成。通用的递归下降分析技术可能需要回溯，也就是说可能需要重复扫描输入。然而，在
// 对程序设计语言的构造进行语法分析时很少需要回溯，因此需要回溯的语法分析器并不常见。即
// 使在自然语言语法分析这样的场合，回溯也不是很高效，因此人们更加倾向于基于表格的方法，
// 例如动态程序规划算法或者 Earley 方法。要支持回溯，首先不能选定唯一的 A 产生式，我们
// 必须按照某个顺序逐个尝试这些产生式，尝试失败仅仅意味着可以尝试另一个 A 产生式。只有
// 当再也没有 A 产生式可以尝试时，才会宣称找到一个输入错误。一个左递归文法会使它的递归
// 下降分析器陷入无限循环中，即使是带有回溯的分析器也是如此。也就是说，当我们试图展开一
