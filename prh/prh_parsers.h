// 问题。因此，一个问题，如果它的语言是递归的，则称此问题是可判定的（decidable），否则
// 称此问题是不可判定的（undecidable）。也就是说，一个问题是不可判定的，如果没有这样的
// 算法，它以问题的实例为输入，并能给出相应的是与否的判定。有时候，将一些不可判定的但是
// 存在图灵机可以识别的语言称为可识别的。
//
// 计算复杂度研究算法的时间复杂度和空间复杂度，有点问题是可判定的，但是其判定算法的复杂
// 度可能是输入规模的指数函数。一般来讲，这种算法在实际应用中是不可取的，一般地我们考虑
// 算法的时间复杂度是否具有多项式复杂度。P 表示确定图灵机在多项式时间内可判定的语言类，
// 这些语言对应的问题称为 P 类问题，这种语言称为多项式可判定的。例如判定一个有向图中的
// 两个顶点之间是否存在有向路径的问题、两个数是否互质的问题、一个字符串是否为一个上下文
// 无关语言的句子问题都是 P 类问题。NP 表示非确定图灵机在多项式时间内可判定的语言类，这
// 些语言对应的问题称为 NP 类问题，也称这些问题是 NP 复杂的或者 NP 困难的，这种语言称为
// 非确定性多项式可判定的。
//
// 由于确定图灵机是一种特殊的非确定图灵机，所以所有 P 类问题都是 NP 问题。但是，是否所
// 有的 NP 类问题都是 P 类问题呢？也就是说 P==NP 成立吗？这个问题是理论计算机科学和当
// 代数学种悬而未决的问题之一。如果这两类相等，那么所有的多项式可验证的问题都将是多项式
// 可判定的，大多数研究人员相信它们不相等。人们发现，NP 类种某些问题的复杂性与整个类的
// 复杂性相关联，如果能找到这些问题中的任何一个的多项式时间判定算法，那么所有的 NP 问题
// 都是多项式时间可判定的。人们称这些问题是 NP 完全问题（NP complete problem）。旅行
// 商问题、划分问题、可满足性问题、带优先次序的调度问题都是典型的 NP 完全问题。
//
// 上下文无关文法分析
//
// 无关文法的规则是递归的，允许包含嵌套结构，例如 if 语句可嵌套包含其他 if 语句，用作识
// 别这些结构的算法也与扫描算法差别很大，必须使用递归调用或显式管理的分析栈。用作表示语
// 法语义结构的数据结构现在也必须是递归的，不再是线性的了，经常使用的基本结构是一类树，
// 称作分析树或语法树。上下文无关文法可以描述程序设计语言中的大部分规则，但不是全部。比
// 如标识符必须先声明后使用、函数实参个数的检查。编译器必须对语法分析器的输出进行分析，
// 以保证源程序遵守那些没有被语法分析器检查的规则。在这些语言的编译器中，标识符是否先声
// 明后使用、函数的一次调用中参数的个数是否正确，通常是在语义分析阶段检查的。
//
// 语法分析涉及到在许多属性和能力截然不同的方法中做出选择，按照它们构造分析树或语法树的    *** 自顶向下和自底向上两种语法分析法
// 方式，算法大致可以分为自顶向下分析法和自底向上分析法。顾名思义，自顶向下分析法从语法
// 分析树的顶部（起始符号根节点）向底部叶子节点一步步蔓延生长，而自底向上分析法则从叶子
// 节点开始，逐步向根节点方向规约和聚合。这两种分析方法中，语法分析器的输入总是从左向右
// 的方式被扫描，每次扫描一个符号。在单遍编译中，分析程序合并编译器中所有其他阶段，这还    *** 单遍编译和多遍编译
// 包括了代码生成，因此也就不需要构造显式的语法树了，分析步骤本身隐式地表示了语法树。在
// 编译器中更多的是多遍，此时后面的遍将语法树作为它们的输入。
//
// 最高效的自顶向下和自底向上方法只能处理某些文法子类，但其中的某些类别，特别是 LL 和     *** LL 文法和 LR 文法
// LR 文法，其表达能力已经足以描述现代程序设计语言的大部分语法结构了。手工实现的语法分
// 析器通常使用 LL 文法，比如预测语法分析方法能够处理 LL 文法。处理较大的 LR 文法类的
// 语法分析器通常是使用自动化工具构造得到的。自顶向下语法分析器之所以受欢迎，是因为这种
// 方法较容易地手工构造出高效的语法分析器。不过，自底向上分析法可以处理更多中文法和翻译
// 方案，所以直接从文法生成语法分析器的软件工具常常使用自底向上的方法。对于任何上下文无
// 关文法，我们都可以构造出一个时间复杂度 O(n^3) 的语法分析器，它最多使用 O(n^3) 时间
// 完成长度 n 的符号串的语法分析。但是，三次方的时间代价太昂贵了，幸运的是对于实际的程
// 序设计语言而言，我们通常能够设计出一个可以被高效分析的文法。线性时间复杂度的算法足以
// 分析实践中出现的各种程序设计语言。程序设计语言的语法分析器几乎总是一次性地从左到右扫
// 描输入，每次向前看一个终结符号，并在扫描时构造出分析树的各个部分。
//
// 自顶向下（top-down）分析法是通过最左推导来分析符号串输入，之所以称自顶向下是由于分析
// 树隐含的编号是一个前序编号，而且其顺序是由根到叶。使用自顶向下的方法构造一棵语法树时，   *** 自顶向下分析法：选择当前语法变量的产生式，寻找下一个最左变量进行推导
// 从起始符号开始，反复执行下面两个步骤，对于某些文法，这些步骤只需对输入串进行一次从左
// 到右的扫描就可以完成。输入中当前被扫描的终结符称为向前看（lookahead）符号，在开始时，
// 向前看符号是输入传中的第一个（最左边的）终结符。
//  1.  在语法变量 A 的节点上，选择 A 的一个产生式，为该产生式中的各个符号创造子节点
//  2.  寻找下一个节点来构造子树，通常选择语法树最左边尚未扩展的非终结符（语法变量）
// 一般来说，为一个语法变量选择产生式是一个尝试并犯错的过程。也就是说，我们首先选择一个
// 产生式，并在这个产生式不合适时进行回溯，再尝试另一个产生式。一个产生式不合适是指使用
// 该产生式后，我们无法构造得到一颗与当前输入串相匹配的语法分析树。但是在称为预测语法分
// 析的特殊情况下不需要进行任何回溯，我们接下来讨论这个方法。
//
// 递归下降分析法（recursive-descent parsing）是一种自顶向下分析法，它使用一组递归过
// 程来处理输入，文法的每个语法变量都有一个相关联的过程。这里我们考虑递归下降分析法的一
// 种简单形式，称为预测分析法（predictive parsing）。在预测分析法种，各个语法变量对应
// 的过程种的控制流可以由向前看符号无二义地确定。在分析输入串时出现的过程调用序列隐式地
// 定义了该输入串的一颗语法分析树。如果需要，还可以通过这些过程调用来构建一个显式的语法
// 分析树。预测分析需要知道哪些符号可能成为多个产生式产生的第一个符号，更精确地说，令 a
// 是一个文法符号串（即包含终结符和语法变量的符号串），我们将 FIRST(a) 定义为可以由 a     *** 符号串 a 的 FIRST 集合：
// 生成的一个或多个终结符号串的第一个符号的集合，如果 a 就是空串或者可以生成空串，那么         如果 a 的第一个符号是终结符，则该终结符是 FIRST 集合的唯一符号
// 空串也在 FIRST(a) 种。我们的预测分析器在没有其他产生式可用时，将空产生式作为默认选         否则 FIRST 集合是 a 第一个语法变量所有产生式的第一个终结符集合
// 择使用。在讨论详细的 FIRST(a) 算法之前，我们使用不具一般性的推导方法来求出 FIRST(a)
// 中的符号。通常情况下，a 要么以一个终结符开头，此时该终结符号就是 FIRST(a) 中的唯一
// 符号；要么 a 以一个语法变量开头，且该语法变量的所有产生式都以某个终结符开头，那么这
// 些终结符就是 FIRST(a) 的所有成员。如果当前的语法变量有两个产生式 A->a B->b，我们就
// 必须考虑相应的 FIRST 集合，如果我们不考虑空产生式，预测分析器要求 FIRST(a) 和
// FIRST(b) 不相交，那么就可以用向前看符号来确定应该使用哪个产生式。如果向前看符号在
// FIRST(a) 中，就是用 a，如果向前看符号在 FIRST(b) 中，就使用 b。因此对于文法的任何
// 语法变量，它的各个产生式的 FIRST 集合应该互不相交。
//
// 如果我们有一个翻译方案，即一个增加了语义动作的文法，那么我们可以将这些语义动作当作此    *** 使用 FIRST 集合的预测分析法
// 语法分析器的过程的一部分执行。一个预测分析器程序有各个语法变量对应的过程组成，对应于
// 语法变量 A 的过程完成以下两项任务：
//  1.  检查向前看符号，决定使用 A 的哪个产生式，如果一个产生式为 A->a 且向前看符号在
//      集合 FIRST(a) 中，那么就选择这个产生式。对于任何向前看符号，如果两个非空产生
//      式之间存在冲突，我们就不能对这种文法使用预测语法分析。另外，如果 A 有空产生式，
//      那么只有当向前看符号不在 A 的其他产生式的 FIRST 集合中时，才使用该空产生式。
//  2.  然后，该语法变量对应的过程模拟被选中的产生式体（body），也就是说，从输入串左边
//      开始逐个执行此产生式中的符号。执行一个语法变量的方法是调用该语法变量对应的过程，
//      一个与向前看终结符匹配的终结符号的执行方法则是读取下一个输入符号。如果在某个点
//      上，产生式中的终结符号与向前看符号不匹配，那么语法分析器就会报告一个语法错误。
//
// 递归下降语法分析器有可能进入无限循环，当出现如下所示的左递归产生式时，分析器会出现      *** 语法变量的左递归派生链
// 无限循环：expr -> expr + term 。在这里，产生式体最左边的符号和产生式头部的语法变量
// 符号相同，将陷入调用 expr 对应的过程的无限循环。通过改写有问题的产生式就可以消除左
// 递归。考虑有两个产生式 A -> Aa | b 的语法变量 A，其中 a 和 b 是不以 A 开头的符号序
// 列，例如产生式 expr -> expr + term | term 。因为产生式 A -> Aa 的右边最左符号是
// 语法变量 A 自身，语法变量 A 和它的产生式被称为是左递归的。不断应用这个产生式将在 A
// 的右边生成一个 a 序列，当 A 最终被替换为 b 时，我们就得到一个在 b 后跟有 0 个或多个
// a 的组略。A -> Aaa -> Aaaa -> baa...a，此时我们使用一个新的语法变量 R，并按照如下
// 方式改下 A 的产生式可以达到同样的效果： A->bR  R->aR|ε ，语法变量 R 和它的产生式是
// 右递归的，因为这个产生式右部的最后一个符号就是 R 本身。右递归的产生式会使得树向右下
// 方向生长，因为树是向右下生长的，对包含了左结合运算符（例如加减）的表达式的翻译会变得
// 较为困难，然后通过仔细设计翻译方案，我们仍然可以将一个表达式正确地翻译成后缀表达式。
//
// 左递归的消除。如果一个文法中有一个语法变量 A 使得对某个串 a 存在一个推导 A =>+ Aa，   *** 从文法上消除左递归
// 那么这个文法就是左递归的（left recursive）。自顶向下语法分析法不能处理左递归的文法，
// 因此需要一个转换法来消除左递归。上面讨论了形如 A->Aa|b 的直接左递归可以替换为右递归
// 的产生式 A->bR R->aR|ε 的情况。这样的替换不会改变可以从 A 推导得到的串的集合，这个
// 规则本身已经足以用来处理很多文法。这里我们研究一般性的情况，因为还存在因为两步或多步
// 推导而产生的左递归情况没有消除，例如考虑文法 S->Aa|b A->Ac|Sd|ε，因为 S=>Aa=>Sda，
// 因此 S 是左递归的，但它不是直接左递归。下面的算法系统地消除文法中的左递归，如果文法
// 中不存在换（即形如 A =>+ A 的推导）或空产生式（即形如 A->ε 的产生式），就能保证能够
// 消除左递归。环和空产生式都可以通过无法文法化简的方法，从文法中系统地消除，文法化简是
// 将空产生式、单一产生式、无用符号去除。作为化简的一个结果，我们可以把一个文法转变为一
// 个没有环的等价文法。对于没有环和空产生式的文法 G，使用以下算法可以得到一个等价的无左
// 递归文法，得到的非左递归文法可以包含空产生式。
//
//      按照某个顺序将语法变量排序为 A0 A1 ... An-1
//      for (i = 0; i < n; i += 1) {
//          for (j = 0; j < i; j += 1) {
//              将每个形如 Ai -> Aj α 的产生式替换为产生式组 Ai -> β1 α | β2 α | ... | βk α
//              其中 Aj -> β1 | β2 | ... | βk 是所有 Aj 的产生式
//          }
//          消除Ai产生式之间的立即左递归（直接左递归），形如 A -> A α1 | A α2 | ... | A αm | β1 | β2 | ... | βn 的产生式替换为：
//          A -> β1 B | β2 B | ... | βn B
//          B -> α1 B | α2 B | ... | αm B | ε
//          其中 β 都不是以 A 开头的符号串
//      }
//
//      例如文法，排序的语法变量 S 和 A：
//          S -> Aa | b
//          A -> Ac | Sd | ε
//
//      处理 i=0，Ai=S
//          语法变量 S 没有如下产生式：S -> S α
//          语法变量 S 的产生式没有 S 的直接左递归
//      处理 i=1，Ai=A，Aj=S
//          A -> Sd 替换为 A -> Aad | bd 变成 A -> Ac | Aad | bd | ε，得到：
//          A -> bdB | B
//          B -> cB | adB | ε
//
//      消除左递归后的文法：
//          S -> Aa | b
//          A -> bdB | B
//          B -> cB | adB | ε
//
// 提取左公因子。提取左公因子是一种文法转换方法，它可以产生适用于预测分析技术或自顶向下    *** 提取最左公因子消除回溯
// 分析技术的文法。当不清楚应该在 A 产生式中如何选择时，我们可以通过改写产生式来推后这
// 个决定，等我们读入了足够多的输入，获得足够信息后再做出正确选择。比如，我们有两个产生
// 式：stmt -> if expr then stmt else stmt | if expr then stmt，看到输入 if 的时候
// 我们不能立即知道选择哪个产生式来展开 stmt。一般地说，如果 A -> α β1 | α β2 是 A 的
// 两个产生式，并且输入的开头式从 α 推导得到的一个非空串，那么我们就不知道应该将 A 展开
// 成 α β1 还是 α β2。然而，我们可以将 A 展开为 α B，从而将做出决定的时间往后延。再读
// 入从 α 推导得到的输入前缀之后，我们再决定将 B 展开为 β1 还是 β2。也就是说，经过提取
// 左公因子，原来的产生变成了： A -> α B   B -> β1 | β2。即对于每个语法变量 A，找出它
// 的两个或多个选项之间的最长公共前缀 α，如果 α != ε，那么存在一个非平凡的公共前缀，则
// 所有 A 的产生式：
//      A -> α β1 | α β2 | ... | α βn | γ
// 替换为：
//      A -> α B | γ
//      B -> β1 | β2 | ... | βn
// 其中 γ 表示所有不以 α 开头的产生式体，B 是一个新的语法变量。不断应用这个转换，直到
// 每个语法变量的任意两个产生式体都没有公共前缀为止。
//
// 使用预测语法分析器时，我们通常需要修改一个给定翻译方案的基础文法，特别地预测语法分析    *** 语法分析树和抽象语法树
// 器不能处理左递归文法。现在我们看起来处在矛盾之中，一方面我们需要一个能够支持翻译规约
// 的文法，另一方面我们又需要一个明显不同支持语法分析过程的文法。解决方法是首先使用易于
// 翻译的文法，然后再小心地对这个文法进行转换，使之能够支持语法分析。通过消除左递归，我
// 们可以得到一个适用于预测递归下降翻译器的文法。设计一个翻译器时，通常使用的数据结构为
// 抽象语法树（AST），在一个表达式的抽象语法树中，每个内部节点代表一个运算符，该节点的
// 子节点代表这个运算符的运算分量。对于更加一般化的情况，当我们处理任意的程序设计语言构
// 造时，我们可以创建一个针对这个构造的运算符，并把这个构造的具有语义信息的组成部分作为
// 这个运算符的运算分量。抽象语法树在某种程度上和语法分析树相似，但是在抽象语法树中，内
// 部节点代表的是程序构造，语法分析树种内部节点代表的是语法变量。文法种很多语法变量都代
// 表程序构造，但也有一部分是各种各样的辅助符号，抽象语法树通常不需要这些辅助符号，因此
// 会将这些符号省略掉。为了强调它们之间的区别，我们有时把语法分析树称为具体语法树（CST），
// 而相应的文法称为该语言的具体语法（concrete syntax）。
//
//      expr = expr + term | expr - term | term
//      term = 0 .. 9
//
//      expr = term rest
//      rest = + term rest | - term rest | ε
//      term = 0 .. 9
//
//      9 - 5 + 2 的语法分析树：
//                  expr
//              term    rest
//           9        -     term    rest
//                           5    +    term  rest
//                                      2     ε
//     抽象语法树（辅助节点和空节点都可以去掉，每个内部节点都和一个运算符关联）：
//              expr                    ==>         +           95-2+
//           9       rest                        -      2
//                 -    5    rest             9     5
